const Flashcards = () => {
  const [flashCards] = useState<Group[]>(flashcards);
  const [expanded, setExpanded] = useState<Set<string>>(new Set());

  const toggleExpand = (id: string) => {
    setExpanded((prev) => {
      const next = new Set(prev);
      if (next.has(id)) {
        next.delete(id);
      } else {
        next.add(id);
      }
      return next;
    });
  };

  return (
    <div className='p-4'>
      <h1 className='text-2xl font-bold mb-4'>Flashcard Groups</h1>
      <TableContainer component={Paper}>
        <Table
          sx={{ minWidth: 650 }}
          size='small'
          aria-label='flashcard groups table'
        >
          <TableHead>
            <TableRow>
              <TableCell sx={{ width: '60px' }}></TableCell>
              <TableCell>Name</TableCell>
              <TableCell align='right' sx={{ width: '120px' }}>
                Total Cards
              </TableCell>
              <TableCell sx={{ width: '120px' }}>Updated At</TableCell>
              <TableCell sx={{ width: '80px' }}>Actions</TableCell>
            </TableRow>
          </TableHead>
          <TableBody>
            {flashCards.map((group) => {
              const isOpen = expanded.has(group.id);
              return (
                <GroupTable
                  key={group.id}
                  group={group}
                  isOpen={isOpen}
                  toggleExpand={toggleExpand}
                  level={0}
                  expanded={expanded}
                />
              );
            })}
          </TableBody>
        </Table>
      </TableContainer>
    </div>
  );
};

export default Flashcards;
